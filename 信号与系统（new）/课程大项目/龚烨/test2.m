    10 / 25 * sin(2 * pi * 174 * 2 * x) + ...
    3 / 25 * sin(2 * pi * 174 * 3 * x) + ...
    2 / 25 *sin(2 * pi * 174 * 4 * x) + ...
    1 / 25 * sin(2 * pi * 174 * 5 * x); 
