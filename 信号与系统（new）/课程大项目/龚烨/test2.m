    10 / 25 * sin(2 * pi * 277 * 2 * x) + ...
    3 / 25 * sin(2 * pi * 277 * 3 * x) + ...
    2 / 25 *sin(2 * pi * 277 * 4 * x) + ...
    1 / 25 * sin(2 * pi * 277 * 5 * x); 
